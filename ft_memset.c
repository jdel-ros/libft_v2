#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*v;

	i = 0;
	v = b;
	while (i < len)
	{
		v[i] = c;
		i++;
	}
	b = v;
	return (b);
}
